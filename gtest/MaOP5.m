function p=MaOP5(p)
 p.name='MaOP5';
 p.od = 10;
 p.pd = 10;
 range           = ones(p.pd,2); 
 range(:,1)      =  0; 
 p.domain        =  range;
 p.func          = @evaluate;
    %P12  evaluation function.
    function ff = evaluate(x)
        [dim, num]   = size(x);    
%         g            = sum((x(3:dim,:) - repmat(x(1,:).*x(2,:),[dim-2,1])/2).^2);
        g            = sum((x(3:dim,:) - x(1,:).*x(2,:)).^2); 
        y            = zeros(p.od,num);
        h            = max(0,-1.4*(cos(2*pi*(x(1,:)))));
        y(1,:)       = cos(0.5*pi*x(1,:)).*cos(0.5*pi*x(2,:));
        y(2,:)       = 4*cos(0.5*pi*x(1,:)).*sin(0.5*pi*x(2,:));
        y(3,:)       = sin(0.5*pi*x(1,:));
        ff(1:2,:)    = (1+g+h).*y(1:2,:);
        ff(8,:)      = (1+g+h).*y(3,:);
        ff(4,:)      = (0.1*y(1,:)+0.1*y(2,:)+0.3*y(3,:)).*(exp((x(4,:)-x(1,:).*x(2,:)).^2)-1);       
        ff(5,:)      = (0.4*y(1,:)+0.1*y(2,:)+0.3*y(3,:)).*(exp((x(5,:)-x(1,:).*x(2,:)).^2)-1);
        ff(6,:)      = (0.3*y(1,:)+0.2*y(2,:)+0.4*y(3,:)).*(exp((x(6,:)-x(1,:).*x(2,:)).^2)-1);
        ff(7,:)      = (0.3*y(1,:)+0.1*y(2,:)+0.1*y(3,:)).*(exp((x(7,:)-x(1,:).*x(2,:)).^2)-1);
        ff(3,:)      = (0.1*y(1,:)+0.3*y(2,:)+0.1*y(3,:)).*(exp((x(8,:)-x(1,:).*x(2,:)).^2)-1);
        ff(9,:)      = (0.1*y(1,:)+0.2*y(2,:)+0.3*y(3,:)).*(exp((x(9,:)-x(1,:).*x(2,:)).^2)-1);
        ff(10,:)     = (0.1*y(1,:)+0.3*y(2,:)+0.1*y(3,:)).*(exp((x(10,:)-x(1,:).*x(2,:)).^2)-1);
        clear Y;
    end
end