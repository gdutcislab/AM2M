function p=MaOP10(p)
 p.name='MaOP10';
 p.od = 10;
 p.pd = 10;%这里可以改为4，如果要求自变量空间是4维的情况
 range = ones(p.pd,2); 
 range(:,1)= 0;
 p.domain  = range;
 p.func    = @evaluate;
    %gf1  evaluation function.
function ff = evaluate(x)    
         [dim, num]   = size(x);
%          g            = sum((x(3:dim,:) - (0.5)./2).^2);
         g            = sum((x(3:dim,:) - (0.5)./2).^2);
         y            = zeros(p.od,num);
     for j=1:num
         if x(1,j)>0.2&&x(1,j)<=0.8&&x(2,j)>0.2&&x(2,j)<=0.8
            y(1,j)   = 2*(1-cos(0.5*pi*x(1,j))).*(1-cos(0.5*pi*x(2,j)));
            y(2,j)   = 2*(1-cos(0.5*pi*x(1,j))).*(1-sin(0.5*pi*x(2,j)));
            y(3,j)   = 2*(1-sin(0.5*pi*x(1,j)));
         else
            y(1,j)   = (1-cos(0.5*pi*x(1,j))).*(1-cos(0.5*pi*x(2,j)));
            y(2,j)   = (1-cos(0.5*pi*x(1,j))).*(1-sin(0.5*pi*x(2,j)));
            y(3,j)   = (1-sin(0.5*pi*x(1,j)));
         end
     end
         ff(1:3,:)    = (1+g).*y(1:3,:);
         ff(4,:)      = (0.1*y(1,:)+0.1*y(2,:)+0.3*y(3,:)).*(exp((x(4,:)-(0.5)./2).^2)-1);
         ff(5,:)      = (0.4*y(1,:)+0.1*y(2,:)+0.3*y(3,:)).*(exp((x(5,:)-(0.5)./2).^2)-1);
         ff(6,:)      = (0.3*y(1,:)+0.2*y(2,:)+0.4*y(3,:)).*(exp((x(6,:)-(0.5)./2).^2)-1);
         ff(7,:)      = (0.3*y(1,:)+0.1*y(2,:)+0.1*y(3,:)).*(exp((x(7,:)-(0.5)./2).^2)-1);
         ff(8,:)      = (0.1*y(1,:)+0.3*y(2,:)+0.1*y(3,:)).*(exp((x(8,:)-(0.5)./2).^2)-1);
         ff(9,:)      = (0.1*y(1,:)+0.2*y(2,:)+0.3*y(3,:)).*(exp((x(9,:)-(0.5)./2).^2)-1);
         ff(10,:)     = (0.1*y(1,:)+0.3*y(2,:)+0.1*y(3,:)).*(exp((x(10,:)-(0.5)./2).^2)-1); 

end
end